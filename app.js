function orientationKey(item) {
  return item.orientation === "逆位" ? "reversed" : "upright";
}

function englishRoleForItem(item) {
  const seed = englishMeaningSeeds[item.card.id]?.[orientationKey(item)];
  if (seed) return seed;
  const phrase = conclusionPhrase(item);
  const matched = conclusionRewriteRulesEn.find(([pattern]) => pattern.test(phrase));
  if (matched) return matched[1];
  return `${cardDisplayName(item.card)} in ${uiText.en.orientations[item.orientation] || item.orientation} position`;
}

function englishContextForItem(item, type) {
  const seed = getSeed(item);
  if (type === "love") {
    return `In relationship context, ${englishRoleForItem(item)}.`;
  }
  const typeLabel = uiText.en.typeLabels[type] || "this topic";
  const contextual = type === "love" ? (seed.love || contextSeed(item, type)) : contextSeed(item, type);
  const matched = conclusionRewriteRulesEn.find(([pattern]) => pattern.test(contextual || ""));
  if (matched) return `In ${typeLabel.toLowerCase()}, ${matched[1]}.`;
  return `In ${typeLabel.toLowerCase()}, ${englishRoleForItem(item)}.`;
}

function englishMeaningSummaryForItem(item) {
  const role = englishRoleForItem(item);
  const orientation = uiText.en.orientations[item.orientation] || item.orientation;
  return `${cardDisplayName(item.card)} ${orientation.toLowerCase()} points to ${role}. Read it as a concrete force in the spread, not only as an abstract mood: it can describe a person, a pattern, an outside condition, or the querent's own way of responding.`;
}

function conclusionIdea(item, fallbackPrefix) {
  const phrase = conclusionPhrase(item);
  if (currentLanguage === "en") {
    const matched = conclusionRewriteRulesEn.find(([pattern]) => pattern.test(phrase));
    return matched ? matched[1] : `${fallbackPrefix}${englishRoleForItem(item)}`;
  }
  const matched = conclusionRewriteRules.find(([pattern]) => pattern.test(phrase));
  if (matched) return matched[1];
  return phrase;
}
function contextSeed(item, type) {
  const seed = getSeed(item);
  if (type === "love" && seed.love) return seed.love;
  return seed.core || firstSentences(item.meaning, 1);
}

